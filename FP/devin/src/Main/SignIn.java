    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed
        // TODO add your handling code here:
        SignInModel SIM = new SignInModel();
        SIM.setUsername(txtUsername.getText());
        SIM.setPassword(txtPassword.getText());
        SIM.setPosition((String) positionBox.getSelectedItem());
        try {
            if (SIM.login().next()){
                JOptionPane.showMessageDialog(null, "Logged in! Press 'OK' to continue");
                if(SIM.getPosition().equals("admin")){
                    Menu_Admin MA = new Menu_Admin();
                    MA.setVisible(true);
                    
                    dispose();
                }
                else if(SIM.getPosition().equals("cashier")){
                    Menu_Cashier_Transaction MCT = new Menu_Cashier_Transaction();
                    MCT.setVisible(true);
                    MCT.lblName.setText(txtUsername.getText());
                    this.dispose();
                }
                else if(SIM.getPosition().equals("stock")){
                    Menu_Admin_Stock MAS = new Menu_Admin_Stock();
                    MAS.setVisible(true);
                    MAS.lblUsername.setText(txtUsername.getText());
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Log in failed! No data found!");
                txtUsername.setText("");
                txtPassword.setText("");
            }
        } catch (SQLException ex) {
            System.out.println("ERROR! "+ex);
        }
    }//GEN-LAST:event_btnSignInActionPerformed

